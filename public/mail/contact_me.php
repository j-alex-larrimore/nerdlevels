<?php

$EmailFrom = Trim(stripslashes(filter_input(INPUT_POST, "email")));
$EmailTo = "nerdlevels@nerdlevels.com";
$Subject = "Contact from NerdLevels";
$Message = Trim(stripslashes(filter_input(INPUT_POST, "message")));
$Name = Trim(stripslashes(filter_input(INPUT_POST, "name")));

// validation
$validationOK=true;
if (!$validationOK) {
  print "<meta http-equiv=\"refresh\" content=\"0;URL=error.htm\">";
  exit;
}

// prepare email body text
$Body = "";
$Body .= "Message: ";
$Body .= $Message;
$Body .= "\n";

// send email 
$success = mail($EmailTo, $Subject, $Body, "From: $Name at $EmailFrom");

// redirect to success page 
if ($success){
    print "<meta http-equiv=\"refresh\" content=\"0;URL=contactthanks.php\">";
}
else{
  print "<meta http-equiv=\"refresh\" content=\"0;URL=error.htm\">";
}
?>